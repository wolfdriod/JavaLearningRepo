/*
 * Question: Write a Java program to declare various primitive data types (int, double, boolean, char, String) and print their values.
 *
 * Input: None (Hardcoded values)
 * Output:
 * Integer Value: 100
 * Double Value: 99.99
 * Boolean Value: true
 * Char Value: A
 * String Value: Java Automation
 */
public class PrintVariables {
    public static void main(String[] args) {
        int intVar = 100;
        double doubleVar = 99.99;
        boolean booleanVar = true;
        char charVar = 'A';
        String stringVar = "Java Automation";

        System.out.println("Integer Value: " + intVar);
        System.out.println("Double Value: " + doubleVar);
        System.out.println("Boolean Value: " + booleanVar);
        System.out.println("Char Value: " + charVar);
        System.out.println("String Value: " + stringVar);
    }
}
